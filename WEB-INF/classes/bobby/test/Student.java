package bobby.test;
public class Student
{
String name;
String rollNo;
public Student()
{
this.name="";
this.rollNo="";
}
public void setName(String name)
{
this.name=name;
}
public String getName()
{
return this.name;
}
public void setRollNo(String rollNo)
{
this.rollNo=rollNo;
}
public String getRollNo()
{
return this.rollNo;
}
}
